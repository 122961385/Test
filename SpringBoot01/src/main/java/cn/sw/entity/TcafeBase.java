    public String getsLegalUser() {
        return sLegalUser;
    }

    public void setsLegalUser(String sLegalUser) {
        this.sLegalUser = sLegalUser;
    }

    public String getsLegalTel() {
        return sLegalTel;
    }

    public void setsLegalTel(String sLegalTel) {
        this.sLegalTel = sLegalTel;
    }


